 	  $data2 = array();
	  $data2["consecutivo"] = $result["consecutivo"];
	  $data2["id_producto"]  = $record['id'];
	  $data2["Cantidad"] = 1;
	  $data2["Precio"] = $record['Precio'];
	  $data2["estado"] = 'Activo';
	  DB::Insert("pedidos", $data2 );
    //Actualizar los articulos pedido
	  unset($sql);
	  unset($rs);
	  unset($data);
	  $sql = "update productos set rgsmrc = 1, cantidad =  1 where id = ".$record['id'];
	  $rs = DB::Exec($sql);     
    // Obtener el ultimo registro insertado
	  unset($sql);
	  unset($rs);
	  unset($data);
	  $sql = "select top 1 id  from pedidos where consecutivo = ".$result["consecutivo"]." order by id desc";
	  $rs = DB::Query($sql);
	  $data = $rs->fetchAssoc();
	  $result["id_pedido"] = $data['id'];   

};
;
	RunnerContext::pop();
	echo my_json_encode($result);
	$button->deleteTempFiles();
