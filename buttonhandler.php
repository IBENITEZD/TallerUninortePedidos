    $result["id_producto"] = $record['id'];  
    // Obtener el ultimo pedido activo
    $sql = "select top 1 consecutivo from pedidos where estado = 'Activo' order by id desc";
    $rs = DB::Query($sql);
    $data = $rs->fetchAssoc();
    if ( $data['consecutivo'] > 0 ) { // Ya tiene pedido activo
		    $result["consecutivo"] = $data['consecutivo'];
    } else {
        $result["consecutivo"] = 0;
			  unset($sql);
